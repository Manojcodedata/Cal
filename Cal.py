def multiply(a,b):
    return a*b

def addition(a,b):
    return a+b

def subtract(a,b):
    return a-b

def divide(a,b):
    return a/b

def modulus(a,b):
    return a%b

def power(a,b):
    return a**b
def floor_division(a,b):
    return a//b 
def square_root(a):
    return a**0.5
def cube_root(a):   
    return a**(1/3) 
def logarithm(a,base):
    import math
    return math.log(a,base) 
def exponential(a):
    import math
    return math.exp(a)